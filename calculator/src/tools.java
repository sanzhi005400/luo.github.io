public class tools {//计算器基本的加减乘除
        public static void sum(double n,double m){
            System.out.println(n+"+"+m+"=" + (n+m));
        }
        public static void reduce(double n,double m){
            System.out.println(n+"-"+m+"=" + (n-m));
        }
        public static void  multiplication(double n,double m){
            System.out.println(n+"*"+m+"=" + (n*m));
        }
        public static void division(double n,double m){
            System.out.println(n+"/"+m+"=" + (n/m));
        }
}
