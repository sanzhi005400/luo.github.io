//取反！和 ^案例演示

public class InverseOperators {
    public static void main(String[] args){
        System.out.println(60>20);//true
        System.out.println(!(60>20));//false


        //逻辑异或
        System.out.println((4<1)^(6>3));//true
        //当a 和 b 不同时为真，否则为假
    }
}
