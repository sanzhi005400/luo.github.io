public class pack {

    //     包的三大作用
    //     1。区分相同名字的类
    //     2.当类很多时，可以很好的管理类【看java api文档】
    //     3.控制访问范围

    //     包的基本语法
    //     package com.hspedu
    //     说明
    //     1.package关键词，表示打包
    //     2.com.hspedu：表示包名

    //     包的本质（原理）
    //     实际上就是创建不同的文件夹来保存类文件


    // 包的命名
    // 规则：
    // 只能包换数字下划线字母小圆点但不能数字开头，不能是关键字
    // 命名规范
    // 一般是小写字母+小圆点
    // com.公司名.项目名.业务模块名
    // com.sina.crm.user  用户模块
    // com.sina.crm.order  订单模块
    // com.sina.crm.utils  工具类



    // 常用的包
    // 一个包下，包含很多的类，java常用的包有
    // java.lang   基本包默认引入不需再引入
    // java.util.*   util包，系统提供的工具包，工具类,使用Scanner
    // java.net* 网络包，网络开发
    // java.awt*  java的界面开发，GUI
}
